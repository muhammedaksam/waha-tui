/**
 * Get a flat list of all emojis from all categories
 */
export function getAllEmojis(): string[] {
  return EMOJI_CATEGORIES.flatMap((c) => c.emojis)
}

/**
 * Search emojis by keyword substring match.
 */
export function searchEmojis(query: string): string[] {
  if (!query || query.trim().length === 0) return getAllEmojis()

  const q = query.toLowerCase().trim()
  const results: string[] = []
  const allEmojis = getAllEmojis()

  for (const emoji of allEmojis) {
    const keywords = EMOJI_KEYWORDS[emoji]
    if (keywords && keywords.includes(q)) {
      results.push(emoji)
    }
  }

  return results
}

/**
 * Get variants for a given base emoji
 */
export function getEmojiVariants(baseEmoji: string): string[] | undefined {
  return EMOJI_VARIANTS[baseEmoji]
}
